package org.hnm736.javabrains;

public class Point {
	int pointX;
	int pointY;
	
	public int getPointX() {
		return pointX;
	}
	public void setPointX(int pointX) {
		this.pointX = pointX;
	}
	public int getPointY() {
		return pointY;
	}
	public void setPointY(int pointY) {
		this.pointY = pointY;
	}
	
	@Override
	public String toString() {
		return "POINT to string returned";
	}
}
